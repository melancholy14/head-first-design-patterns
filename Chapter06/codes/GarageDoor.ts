class GarageDoor {
    up() {
        console.log('차고 문이 열렸습니다');
    }

    down() {
        console.log('차고 문이 닫혔습니다');
    }

    stop() {
        console.log('차고 문이 멈췄습니다');
    }

    lightOn() {
        console.log('차고 문의 조명이 켜졌습니다');
    }

    lightOff() {
        console.log('차고 문의 조명이 꺼졌습니다');
    }
}

export default GarageDoor;
